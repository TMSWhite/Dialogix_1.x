package org.dianexus.triceps;

public class SandBoxItem {
	
	private int id;
	private int sandbox_id;
	private int instrument_id;
	private int instrument_version_id;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getInstrument_id() {
		return instrument_id;
	}
	public void setInstrument_id(int instrument_id) {
		this.instrument_id = instrument_id;
	}
	public int getInstrument_version_id() {
		return instrument_version_id;
	}
	public void setInstrument_version_id(int instrument_version_id) {
		this.instrument_version_id = instrument_version_id;
	}
	public int getSandbox_id() {
		return sandbox_id;
	}
	public void setSandbox_id(int sandbox_id) {
		this.sandbox_id = sandbox_id;
	}
	
	

}
